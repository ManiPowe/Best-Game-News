<?php
session_start();
require_once('db.php');

// ==========================================
// ШАГ 1: ПРОВЕРКА АВТОРИЗАЦИИ
// ==========================================
// В будущем здесь будет проверка $_SESSION['user_id']
// Пока для теста захардкодим ID = 1
if (!isset($_SESSION['user_id'])) {
    // Временно для теста
    $user_id = 1;
} else {
    $user_id = $_SESSION['user_id'];
}

// ==========================================
// ШАГ 2: ПРОВЕРКА, БЫЛ ЛИ ФАЙЛ ЗАГРУЖЕН
// ==========================================
// $_FILES['avatar'] - массив с информацией о загруженном файле
// 'avatar' - это значение атрибута name в <input type="file" name="avatar">
if (!isset($_FILES['avatar']) || $_FILES['avatar']['error'] !== UPLOAD_ERR_OK) {
    // UPLOAD_ERR_OK = 0 означает успешную загрузку
    die("Ошибка загрузки файла. Код ошибки: " . $_FILES['avatar']['error'] . " <a href='../cab.php'>Назад</a>");
}

// ==========================================
// ШАГ 3: ПОЛУЧАЕМ ИНФОРМАЦИЮ О ФАЙЛЕ
// ==========================================
$file = $_FILES['avatar'];

// Разбираем $_FILES по косточкам:
// $file['name']     - оригинальное имя (например, "мой_аватар.png")
// $file['type']     - MIME-тип от браузера (например, "image/png") - НЕ ДОВЕРЯЙ ЕМУ!
// $file['tmp_name'] - временный путь на сервере (например, "/tmp/php1A2B3C")
// $file['error']    - код ошибки (0 = всё хорошо)
// $file['size']     - размер в байтах

$original_name = $file['name'];
$tmp_path = $file['tmp_name'];
$file_size = $file['size'];

// ==========================================
// ШАГ 4: ПРОВЕРКА РАЗМЕРА ФАЙЛА
// ==========================================
// Ограничиваем размер до 5 МБ (5 * 1024 * 1024 байт)
$max_size = 5 * 1024 * 1024;
if ($file_size > $max_size) {
    die("Файл слишком большой! Максимум 5 МБ. <a href='../cab.php'>Назад</a>");
}

// ==========================================
// ШАГ 5: ПРОВЕРКА ТИПА ФАЙЛА (БЕЗОПАСНОСТЬ!)
// ==========================================
// Получаем расширение файла (всё после последней точки)
// pathinfo() возвращает массив с информацией о пути
// PATHINFO_EXTENSION - константа, указывающая, что нам нужно только расширение
$extension = strtolower(pathinfo($original_name, PATHINFO_EXTENSION));

// "Белый список" разрешенных расширений
$allowed_extensions = ['jpg', 'jpeg', 'png', 'webp', 'gif'];

// in_array() проверяет, есть ли значение в массиве
if (!in_array($extension, $allowed_extensions)) {
    die("Разрешены только файлы форматов: JPG, PNG, WEBP, GIF. <a href='../cab.php'>Назад</a>");
}

// Дополнительная проверка: убеждаемся, что это ДЕЙСТВИТЕЛЬНО картинка
// getimagesize() читает заголовок файла и возвращает массив с размерами
// Если файл не является изображением, вернет false
$image_info = getimagesize($tmp_path);
if ($image_info === false) {
    die("Файл не является изображением! <a href='../cab.php'>Назад</a>");
}

// ==========================================
// ШАГ 6: ГЕНЕРАЦИЯ УНИКАЛЬНОГО ИМЕНИ ФАЙЛА
// ==========================================
// НИКОГДА не сохраняй файл под именем пользователя!
// Причины:
// 1) Два пользователя загрузят "avatar.jpg" - второй перезапишет первого
// 2) Злоумышленник загрузит "hack.php.jpg" - если сервер ошибется, выполнит PHP-код

// uniqid() - генерирует уникальное имя на основе текущего времени в микросекундах
// random_bytes(4) - 4 случайных байта (8 символов в hex)
// bin2hex() - преобразует бинарные данные в hexadecimal строку
$unique_name = 'avatar_' . uniqid() . '_' . bin2hex(random_bytes(4)) . '.' . $extension;
// Пример результата: avatar_65a3f2b8c1d9e_a1b2c3d4.jpg

// ==========================================
// ШАГ 7: ПЕРЕНОС ФАЙЛА В ПАПКУ ПРОЕКТА
// ==========================================
// Путь к папке для аватаров (относительно корня проекта)
$upload_dir = '../Media/avatars/';
$destination = $upload_dir . $unique_name;

// move_uploaded_file() - специальная функция для безопасного перемещения загруженных файлов
// Она проверяет, что файл действительно был загружен через HTTP POST
if (!move_uploaded_file($tmp_path, $destination)) {
    die("Не удалось сохранить файл на сервере. <a href='../cab.php'>Назад</a>");
}

// ==========================================
// ШАГ 8: УДАЛЕНИЕ СТАРОГО АВАТАРА (ОПЦИОНАЛЬНО)
// ==========================================
// Чтобы не засорять диск, удалим старый аватар, если он не стандартный
$sql_old = "SELECT avatar FROM users WHERE id = ?";
$stmt_old = mysqli_prepare($conn, $sql_old);
mysqli_stmt_bind_param($stmt_old, "i", $user_id); // "i" = integer (число)
mysqli_stmt_execute($stmt_old);
$result_old = mysqli_stmt_get_result($stmt_old);
$old_data = mysqli_fetch_assoc($result_old);

// file_exists() проверяет, существует ли файл на диске
if ($old_data && $old_data['avatar'] !== 'assets/Media/Photo/man.png' && file_exists($old_data['avatar'])) {
    unlink($old_data['avatar']); // unlink() удаляет файл с диска
}

// ==========================================
// ШАГ 9: ОБНОВЛЕНИЕ БАЗЫ ДАННЫХ
// ==========================================
// Сохраняем путь к новому аватару в базу
// Путь должен быть относительно корня проекта для корректного отображения в HTML
$new_avatar_path = 'assets/Media/avatars/' . basename($destination);

$sql_update = "UPDATE users SET avatar = ? WHERE id = ?";
$stmt_update = mysqli_prepare($conn, $sql_update);
mysqli_stmt_bind_param($stmt_update, "si", $new_avatar_path, $user_id); // "s" = строка, "i" = число

if (mysqli_stmt_execute($stmt_update)) {
    // Успех! Возвращаемся в профиль
    header("Location: ../cab.php");
    exit;
} else {
    die("Ошибка обновления базы данных: " . mysqli_error($conn));
}
?>